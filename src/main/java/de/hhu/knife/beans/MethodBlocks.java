package de.hhu.knife.beans;

import java.util.List;

public class MethodBlocks {

	private List<String> methodBlocks;

	public MethodBlocks(final List<String> methodBlocks) {
		this.methodBlocks = methodBlocks;
	}

	public List<String> getMethodBlocks() {
		return methodBlocks;
	}

	public void setMethodBlocks(List<String> methodBlocks) {
		this.methodBlocks = methodBlocks;
	}

}
